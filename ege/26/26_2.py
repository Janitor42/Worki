file = open('26_2_file.txt', 'r')
file = file.readlines()[1:]

data = {}

for i in file:
    x = list(map(int, i.strip().split(' ')))
    data[x[0]] = x

queue1 = []
queue2 = []

who_left = 0
time = 0
clients_win_1 = 0


def check_queue(queue):
    global who_left
    if len(queue) == 0:
        time_finish = time_reception + time_visit
        queue.append(time_finish)
    elif len(queue) <= 11:
        time_finish = time_reception + queue[-1]
        queue.append(time_finish)
    else:
        who_left += 1


while time != 24 * 60:
    if time in queue1:
        queue1.remove(time)
    if time in queue2:
        queue2.remove(time)

    if time not in data:
        time += 1
        continue

    window = data[time][2]
    time_reception = data[time][1]
    time_visit = data[time][0]

    if window == 0:
        if len(queue1) > len(queue2):
            window = 2
        else:
            window = 1

    if window == 1:
        old_queue = len(queue1)
        check_queue(queue=queue1)
        new_queue = len(queue1)

        if new_queue != old_queue:
            clients_win_1 += 1

    elif window == 2:
        check_queue(queue=queue2)
    time += 1

