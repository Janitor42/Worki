# Каждая из следующих N строк описывает одного клиента и содержит 3 целых числа:
# время прихода клиента в отделение (количество минут с начала рабочего дня),
# время, необходимое для обслуживания данного клиента, и номер окна, в которое ему необходимо обратиться
# (0 означает, что клиент может обратиться в любое окно). Гарантируется, что никакие два клиента не приходят одновременно.
#
# Определите, сколько клиентов будет обслужено в течение дня в окне номер 1 и сколько клиентов покинет отделение из-
# за слишком больших очередей.
#
# В ответе запишите два целых числа:
# сначала количество клиентов, обслуженных в окне номер 1, затем количество необслуженных клиентов.
