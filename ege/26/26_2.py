# при обращении в любое окно.
# Клиент входит в отделение и встаёт в очередь к тому окну,
# которое оказывает необходимую ему услугу. Если услуга может быть оказана в любом окне,
# клиент выбирает то, в очереди к которому в данный момент меньше людей. Если очереди в оба окна одинаковые,
# клиент выбирает окно с меньшим номером. При этом если в очереди к выбранному окну уже стоит 12 или более человек
# (включая человека, которого обслуживают в данный момент), пришедший клиент сразу уходит.

# Если момент завершения обслуживания одного или нескольких клиентов совпадает с моментом прихода нового клиента,
# то можно считать, что новый клиент пришёл после того,
